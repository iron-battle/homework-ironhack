package RPG.utils;

// Clase utilitaria para definir códigos ANSI de colores para la consola
public class AnsiColors {
    // Código para resetear el color al valor por defecto
    public static final String RESET = "\u001B[0m";
    // Código ANSI para color rojo (usado para Player 1)
    public static final String RED   = "\u001B[31m";
    // Código ANSI para color azul (usado para Player 2)
    public static final String BLUE  = "\u001B[34m";
    // Código ANSI para color verde (usado para mensajes del sistema)
    public static final String GREEN = "\u001B[32m";
}
